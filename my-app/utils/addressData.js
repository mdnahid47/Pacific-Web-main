// Utility function to get district name by key
export const getDistrictName = (key) => {
  for (const division in districts) {
    const found = districts[division].find(d => d.key === key);
    if (found) return found.value;
  }
  return '';
};

// Utility function to get thana name by key
export const getThanaName = (key) => {
  for (const district in thanas) {
    const found = thanas[district].find(t => t.key === key);
    if (found) return found.value;
  }
  return '';
};